public class AsciiArt {

	/**
	 * Prints my name in ASCII Art to the console.
	 * @param args Command-line arguments are ignored.
	 */
	public static void main(String[] args) {
		System.out.println("-----");
		System.out.println("|     \\   -----     |-----    /-----\\     -----");
		System.out.println("|      |    |       |         |       |  /     \\");
		System.out.println("|      |    |       |-----     \\____//   |     |");
		System.out.println("|     /     |       |              / /   |     |");
		System.out.println("-----     -----     |_____     __ / /    \\_____/");
		System.out.println("                              \\\\ / /");
		System.out.println("                                \\/");

	}

}
